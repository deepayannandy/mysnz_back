const mongoos=require("mongoose")
const customerHistorySchema= new mongoos.Schema({
    customerId:{
        type:String,
        required:true
    },
    date:{
        type:Date,
        required:true
    },
    customerName:{
        type:String,
        required:true
    },
    description:{
        type:String,
        required:true
    },
    quantity:{
        type:Number,
        required:true
    },
    discount:{
        type:Number,
        require:true
    },
    netPay:{
        type:Number,
        required:true
    },
    paid:{
        type:Number,
        required:true
    },
    due:{
        type:Number,
        required:false
    },
    startTime:{
        type:Date,
        required:true
    },
    endTime:{
        type:Date,
        required:true
    },
})
module.exports=mongoos.model('CustomerHistory',customerHistorySchema)